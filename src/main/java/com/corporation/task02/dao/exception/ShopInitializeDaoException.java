package com.corporation.task02.dao.exception;

public class ShopInitializeDaoException extends Exception {

	private static final long serialVersionUID = 1L;

	public ShopInitializeDaoException() {
		super();
	}

	public ShopInitializeDaoException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public ShopInitializeDaoException(String message, Throwable cause) {
		super(message, cause);
	}

	public ShopInitializeDaoException(String message) {
		super(message);
	}

	public ShopInitializeDaoException(Throwable cause) {
		super(cause);
	}
	
		
}
